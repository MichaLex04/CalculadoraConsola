namespace CalculadoraConsola.Logica
{
    public class Operaciones
    {
        public int Multiplicacion(int n1 , int n2) => n1*n2;

        public int Division(int n1 , int n2) => n1/n2;

        public int Suma(int n1 , int n2)=> n1+n2;

        public int Resta (int n1 , int n2)=> n1-n2;
    }
}